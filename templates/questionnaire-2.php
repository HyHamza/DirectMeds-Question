    const WeightLossAdvocatesForm = document.getElementById("WeightLossAdvocatesForm");
    const progressBar = document.querySelector(".progress-bar");

    function nextStep() {
        let passed = validateStep();
        if (passed) {
            document.getElementById('WeightLossAdvocatesForm').submit();
        }
    }

    function setProgressValues(step) {
        if (progressBar) {
            progressBar.style.width = ((step - 1) / (14 + 3)) * 100 + "%";
        }
    }

    function triggerBadResponseMessage() {
        let generalWarning = document.getElementById('generalWarning');
        generalWarning.style.display = 'block';
    }

    function addPulse(elements) {
        if (typeof elements.forEach !== 'function') {
            elements = [elements];
        }
        elements.forEach((item) => {
            item.classList.add("pulse");
        });
    }

    function removePulse(elements) {
        if (typeof elements.forEach !== 'function') {
            elements = [elements];
        }
        elements.forEach((item) => {
            item.classList.remove("pulse");
        });
    }

    function handleYesNo(name) {
        var elements = document.querySelectorAll('[name="'+name+'"]');
        const selectedValue = document.querySelector('input[name="'+name+'"]:checked')?.value;
        if (selectedValue == 'no') {
            removePulse(elements);
            return true;
        } else if (selectedValue == 'yes') {
            triggerBadResponseMessage();
        }
        elements.forEach((el) => {
            addPulse(el);
        });
        return false;
    }

    setProgressValues(2);

    function hideElement(id) {
        document.getElementById(id).style.display = 'none';
    }
    document.getElementById('closeBmi').addEventListener('click',function(){
        hideElement('bmiCorrection');
    });
    document.getElementById('closeWarning').addEventListener('click',function(){
        hideElement('generalWarning');
    });
        function validateStep() {
        let month = document.querySelector('[name="intake_dob_month"]');
        let day = document.querySelector('[name="intake_dob_day"]');
        let year = document.querySelector('[name="intake_dob_year"]');
        let elements = [month,day,year];
        removePulse(elements);
        var passed = true;
        elements.forEach((el) => {
            if (el.value == "") {
                addPulse(el);
                passed = false;
            }
        });
        return passed;
    }

    function populateDays() {
        const daySelect = document.querySelector('[name="intake_dob_day"]');
        const month = document.querySelector('[name="intake_dob_month"]').value;
        const year = document.querySelector('[name="intake_dob_year"]').value;
        const daysInMonth = new Date(year != "" ? year : ((new Date().getFullYear()) - 18), month != "" ? month : 1, 0).getDate();
        let selectedDay = daySelect.value;

        daySelect.innerHTML = '';
        let defaultOption = document.createElement('option');
        defaultOption.value = "";
        defaultOption.textContent = "Day";
        defaultOption.selected = true;
        defaultOption.disabled = true;
        daySelect.appendChild(defaultOption);

        for (let day = 1; day <= daysInMonth; day++) {
            const option = document.createElement('option');
            option.value = day;
            option.textContent = day;
            daySelect.appendChild(option);
        }

        if (selectedDay != "" && selectedDay <= daysInMonth) {
            document.querySelector('[name="intake_dob_day"] option[value="'+selectedDay+'"]').selected = true;
        }
    }

    document.querySelectorAll('[name="intake_dob_month"],[name="intake_dob_year"]').forEach((el) => {el.addEventListener('change', function () {
        populateDays();
    })});</script>
